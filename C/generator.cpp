mt19937 rng(chrono::steady_clock::now().time_since_epoch().count());

int get_rand(int l, int r) {
	uniform_int_distribution<int> uid(l, r);
	return uid(rng);
}

void test_case() {
  int n = get_rand(2, 1e5);
  cout << n << "\n";
  while (n--) {
    cout << get_rand(1, 1e5) << " ";
  }
  cout << "\n";
}

